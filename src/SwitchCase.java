public class SwitchCase {
    public static void main(String[] args) {
        int a = 1;
        switch (a) {
            case 1:
                System.out.println("ini 1");
                break;
            case 2:
                System.out.println("ini 2");
                break;
            default:
                System.out.println("ini default");
        }
    }
}
